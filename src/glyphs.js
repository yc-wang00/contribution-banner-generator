export function glyphWidth(char) {
  return (FONT_5X7[char] || FONT_5X7[" "])[0].length;
}

export function textPixelWidth(text, letterGap) {
  return text
    .toUpperCase()
    .split("")
    .reduce((sum, char, index) => {
      return sum + glyphWidth(char) + (index === text.length - 1 ? 0 : letterGap);
    }, 0);
}

/** Render text centered into a rows x weeks grid, marking lit pixels with value 4. */
export function textToGrid(text, weeks = 53, rows = 7, letterGap = 1) {
  const safeText = text || "yicheng";
  const grid = Array.from({ length: rows }, () => Array(weeks).fill(0));
  const width = textPixelWidth(safeText, letterGap);
  let cursor = Math.max(0, Math.floor((weeks - width) / 2));

  safeText
    .toUpperCase()
    .split("")
    .forEach((char) => {
      const glyph = FONT_5X7[char] || FONT_5X7[" "];
      const w = glyph[0].length;
      if (cursor + w > weeks) return;

      for (let y = 0; y < rows; y += 1) {
        for (let x = 0; x < w; x += 1) {
          if (glyph[y][x] === "1") grid[y][cursor + x] = 4;
        }
      }

      cursor += w + letterGap;
    });

  return grid;
}
